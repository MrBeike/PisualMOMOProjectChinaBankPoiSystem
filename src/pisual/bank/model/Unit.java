package pisual.bank.model;

import java.io.File;
import java.util.List;

public class Unit {
	private File SourceFile;
	private int TargetLocationX;
	private int TargetLocationY;
	private double result;
	private List<SourceLocation> sourceLocation;
	public File getSourceFile() {
		return SourceFile;
	}
	public void setSourceFile(File sourceFile) {
		SourceFile = sourceFile;
	}
	public int getTargetLocationX() {
		return TargetLocationX;
	}
	public void setTargetLocation(int targetLocationX,int targetLocationY) {
		TargetLocationX = targetLocationX;
		TargetLocationY = targetLocationY;
	}
	public int getTargetLocationY() {
		return TargetLocationY;
	}
	public List<SourceLocation> getSourceLocation() {
		return sourceLocation;
	}
	public void setSourceLocation(List<SourceLocation> sourceLocation) {
		this.sourceLocation = sourceLocation;
	}
	
	public double getResult() {
		return result;
	}
	public void setResult(double result) {
		this.result = result;
	}
	public void setTargetLocationX(int targetLocationX) {
		TargetLocationX = targetLocationX;
	}
	public void setTargetLocationY(int targetLocationY) {
		TargetLocationY = targetLocationY;
	}
	
}
